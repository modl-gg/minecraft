package gg.modl.minecraft.core.boot;

import lombok.Data;
import org.yaml.snakeyaml.Yaml;

import java.io.IOException;
import java.io.InputStream;
import java.io.Writer;
import java.nio.file.Files;
import java.nio.file.Path;
import java.util.Map;

@Data
public class BootConfig {
    static final String PLACEHOLDER_API_KEY = "your-api-key-here";

    private Mode mode = Mode.STANDALONE;
    private String apiKey = "";
    private boolean testingApi = false;
    private String proxyType;
    private int bridgePort = 25590;

    // Bridge-only proxy connection settings captured by the setup wizard and persisted in boot.yml
    private String wizardProxyHost;
    private int wizardProxyPort = 25590;

    public enum Mode {
        STANDALONE,
        BRIDGE_ONLY,
        PROXY;

        public static Mode fromString(String value) {
            if (value == null) return STANDALONE;
            String normalized = value.toLowerCase().replace("_", "-");
            if (BootConfigYaml.MODE_BRIDGE.equals(normalized)) return BRIDGE_ONLY;
            if (BootConfigYaml.MODE_BRIDGE_ONLY.equals(normalized)) return BRIDGE_ONLY;
            if (BootConfigYaml.MODE_PROXY.equals(normalized)) return PROXY;
            return STANDALONE;
        }

        public String toYaml() {
            if (this == STANDALONE) return BootConfigYaml.MODE_STANDALONE;
            if (this == BRIDGE_ONLY) return BootConfigYaml.MODE_BRIDGE_ONLY;
            return BootConfigYaml.MODE_PROXY;
        }
    }

    public boolean isValid() {
        if (apiKey == null || apiKey.isEmpty() || apiKey.equals(PLACEHOLDER_API_KEY)) {
            return mode == Mode.BRIDGE_ONLY; // bridge-only can work without API key for now
        }
        return true;
    }

    public static boolean exists(Path dataDir) {
        return Files.exists(dataDir.resolve(BootConfigYaml.FILE_NAME));
    }

    @SuppressWarnings("unchecked")
    public static BootConfig load(Path dataDir) throws IOException {
        Path file = dataDir.resolve(BootConfigYaml.FILE_NAME);
        if (!Files.exists(file)) return null;

        Yaml yaml = new Yaml();
        try (InputStream is = Files.newInputStream(file)) {
            Map<String, Object> data = yaml.load(is);
            if (data == null) return null;
            return BootConfigYaml.fromMap(data);
        }
    }

    public void save(Path dataDir) throws IOException {
        Path file = dataDir.resolve(BootConfigYaml.FILE_NAME);
        if (!Files.exists(dataDir)) Files.createDirectories(dataDir);

        Yaml yaml = BootConfigYaml.createYaml();
        Map<String, Object> map = BootConfigYaml.toMap(this);

        try (Writer writer = Files.newBufferedWriter(file)) {
            writer.write("# Auto-generated by setup wizard. Delete to re-run wizard.\n");
            yaml.dump(map, writer);
        }
    }

    public static void saveTemplate(Path dataDir) throws IOException {
        Path file = dataDir.resolve(BootConfigYaml.FILE_NAME);
        if (!Files.exists(dataDir)) Files.createDirectories(dataDir);
        try (Writer writer = Files.newBufferedWriter(file)) {
            writer.write(BootConfigYaml.template());
        }
    }
}
